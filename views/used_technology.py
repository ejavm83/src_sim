"""이 앱에 쓰인 핵심 기술(SimPy, OR-Tools CP-SAT, Streamlit) — 비전문가용 설명."""

from __future__ import annotations

import streamlit as st


def render() -> None:
    st.header("📘 이 앱에서 사용된 기술")

    st.subheader("1) SimPy — “가상 공장 시계”를 돌리는 엔진")
    st.markdown(
        """
**한 줄로:** 실제 공장을 세우지 않고, 컴퓨터 안에서 **트럭·설비·대기 줄이 시간 순서대로 움직이는 장난감 세트**처럼 재현하는 도구입니다.

**비유:**  
- 각 트럭·반사로·압착기는 **각자 할 일이 있는 사람**처럼 따로 움직입니다.  
- 계근대는 **한 번에 한 대만 올라갈 수 있는 다리** 같고, 버퍼(창고)는 **자리가 정해진 대기열** 같습니다.  
- SimPy는 “지금 몇 시인지”, “누가 누구를 기다리는지”를 **분 단위로 계속 밀어 나가며** 기록합니다.

**이 앱에서 하는 일:**  
입고 → 선별·압착 → 장입·용해 → 주조 → 출하까지 **어느 설비가 얼마나 바쁜지**, **어디서 줄이 서는지**를 이 규칙으로 계산합니다.  
(코드에서는 설비를 `Resource`, 창고·대기열을 `Store` 같은 형태로 표현합니다.)

**조금만 더 깊게**

- **이산사건**이란 “매 초마다”가 아니라, **‘트럭이 도착했다’ ‘용해가 끝났다’**처럼 **의미 있는 순간**을 중심으로 시계를 진행한다는 뜻에 가깝게 이해하시면 됩니다.
- SimPy는 **웹 화면을 만드는 프로그램이 아니라**, “뒤에서 숫자를 굴리는” **시뮬레이션 계산 엔진**입니다.
"""
    )

    st.divider()
    st.subheader("2) OR-Tools CP-SAT — “순서 짜기”를 수학으로 도와주는 도구")
    st.markdown(
        """
**한 줄로:** “반사로에 들어갈 작업 순서를 **서로 겹치지 않게** 짤 때, 이론상 **전체가 가장 빨리 끝나는 쪽**에 얼마나 가까운지”를 **별도로** 살펴볼 때 쓰는 **최적화(조합 찾기) 도구**입니다.

**비유:**  
- SimPy가 **실제 규칙(줄 서기, 설비 점유)**으로 돌린 **현실적인 결과**라면,  
- CP-SAT는 같은 조건을 **퍼즐처럼 풀어** “순서만 바꿔도 **최대한 빨리 끝낼 수 있는 한계**가 어디쯤인가?”를 **비교용 답**으로 보여 주는 계산기에 가깝습니다.

**이 앱에서 하는 일:**  
공장 **전체**를 CP-SAT로 바꾸지는 않습니다. **반사로 일정**처럼 “순서가 성능에 크게 영향을 주는 부분”에 대해, SimPy 결과와 **나란히 보며 통찰**할 때 활용합니다.  
(이름의 **CP**는 제약을 풀고, **SAT**는 논리·조건을 만족하는 배치를 찾는 쪽의 기술 계열을 가리킵니다. 세부는 몰라도 사용에는 지장 없습니다.)
"""
    )

    st.divider()
    st.subheader("3) Streamlit — 지금 보고 계신 **웹 대시보드**를 만드는 도구")
    st.markdown(
        """
**한 줄로:** Python 코드로 **버튼·그래프·표·탭**이 있는 화면을 빠르게 만들어, **브라우저**로 여는 방식입니다.

**비유:**  
- 엑셀 매크로로 표를 만드는 것과 비슷하게, **“코드로 화면을 그린다”**고 생각하시면 됩니다.  
- 사용자가 숫자를 바꾸고 실행을 누르면, 서버에서 다시 계산하고 **화면을 갱신**합니다.

**이 앱에서 하는 일:**  
사이드바의 시뮬 조건, 시뮬레이션 실행 버튼, 결과 차트 등 **대부분의 화면**이 Streamlit 기반입니다.  
차트·표 쪽은 **Plotly**, 데이터 가공은 **Pandas** 같은 친구 라이브러리를 함께 씁니다(이름만 알아 두셔도 충분합니다).

**일반인에게 중요한 점:**  
Streamlit 자체가 “공장을 예측한다”기보다, **SimPy가 낸 결과를 보기 좋게 보여 주고**, 버튼으로 **다시 돌려 보게** 해 주는 **창문**에 가깝습니다.
"""
    )

    st.info(
        "**정리:** SimPy = 가상 공장 돌리기 · OR-Tools CP-SAT = 순서·일정의 이론적 비교 · "
        "Streamlit = 그 결과를 보여 주는 웹 화면."
    )
